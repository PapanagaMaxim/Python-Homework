print(TPA1)
# CODUL TĂU VINE MAI SUS:

# Acum utilizați operatorul `not` pentru a verifica dacă `is_student` este `False` și tipăriți rezultatul folosind funcția `print`

# CODUL TĂU VINE MAI JOS:
TPA2 = not is_student
