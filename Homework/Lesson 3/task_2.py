# CODUL TĂU VINE MAI SUS:

# Acum utilizați operatorul `or` pentru a verifica dacă cel puțin una dintre `is_student` și `is_child` este `True` și tipăriți rezultatul folosind funcția `print`

# CODUL TĂU VINE MAI JOS:
